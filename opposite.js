// Opposite Number 

// Difficulty: 8-kyu 

// Language: JavaScript

// Topic: Fundamentals

// Description: 

// Very simple, given an integer or a floating-point number, find its opposite.

// Examples:

// 1: -1
// 14: -14
// -34: 34

// Solution:

function opposite(number) {
    // Use the multiplication operator
    return -1 * number;
}